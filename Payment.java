package onlineshopping.java;

public class Payment {
			private float amt;
			private char date;
			private float paymentno;
			public float getAmt() {
				return amt;
			}
			public void setAmt(float amt) {
				this.amt = amt;
			}
			public char getDate() {
				return date;
			}
			public void setDate(char date) {
				this.date = date;
			}
			public float getPaymentno() {
				return paymentno;
			}
			public void setPaymentno(float paymentno) {
				this.paymentno = paymentno;
			}
			
}
