package onlineshopping.java;

public class Admin {
	private int id;
	private char name;
	private char emailid;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public char getName() {
		return name;
	}
	public void setName(char name) {
		this.name = name;
	}
	public char getEmailid() {
		return emailid;
	}
	public void setEmailid(char emailid) {
		this.emailid = emailid;
	}
	

}
